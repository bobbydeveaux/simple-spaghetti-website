/**
 * Get all unique makes from the vehicles array.
 * @returns {string[]}
 */
export function getUniqueMakes() {
  return [...new Set(cars.map((c) => c.make))].sort();
}

/**
 * Get all unique salvage categories.
 * @returns {string[]}
 */
export function getUniqueCategories() {
  return [...new Set(cars.map((c) => c.salvageCategory))].sort();
}

/**
 * Get price bounds across all vehicles.
 * @returns {{ min: number, max: number }}
 */
export function getPriceBounds() {
  const prices = cars.map((c) => c.price);
  return { min: Math.min(...prices), max: Math.max(...prices) };
}
